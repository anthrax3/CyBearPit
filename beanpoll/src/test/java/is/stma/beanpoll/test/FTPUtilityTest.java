import java.io.File;

@RunWith(Arquillian.class)
public class FTPUtilityTest {

    @Deployment
    public static Archive<?> createTestArchive() {

        File[] files = Maven.resolver().loadPomFromFile("pom.xml")
                .importRuntimeDependencies().resolve().withTransitivity().asFile();

        return ShrinkWrap.create(WebArchive.class, "testDNSUtility.war")
                .addPackage(FTPUtility.class.getPackage())
                .addAsResource("META-INF/test-persistence.xml", "META-INF/persistence.xml")
                .addAsWebInfResource(EmptyAsset.INSTANCE, "beans.xml")
                .addAsWebInfResource("test-ds.xml") // Deploy test datasource
                .addAsLibraries(files); // Add necessary stuff from pom.xml
    }

    @Test
    public void testFTPDownload() {
        String results = FTPUtility.readFTPTextFile("test.rebex.net", 21, "demo", "password", "readme.txt");
        Assert.assertEquals("", results);
    }
}
